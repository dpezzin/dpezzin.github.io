# This function takes two arguments, at positions 0 and 1
def divide(x,y):
    return x/y

# 5 is assigned to x, and 1 is assigned to y based on positions
print(divide(5,1))

# 1 is assigned to x, and 5 is assigned to y based on positions.
print(divide(1,5))

